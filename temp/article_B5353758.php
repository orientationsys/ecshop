                   <div class="stk-post stk-layout_nocol stk-theme_1445"><h1>新生儿脐带护理</h1>
<p><b>【导读】</b>还在娘胎时，脐带是胎儿和妈妈&ldquo;沟通&rdquo;的桥梁，宝宝出生后，护士会在靠近宝宝的一端剪断，留下一段脐带残端。新生儿脐带护理可不是件容易的事情，这个&ldquo;受伤&rdquo;的小坑一旦没有被照顾好，就会招来破伤风、败血症等疾病。婴儿脐带护理最好的防御手段就是每天都用酒精给脐窝擦拭消毒，勤换肚脐贴，大概1-2周期待残端就会自动脱落啦，你会看到宝宝漂亮的肚脐正式形成！ <img src="../../../storage/images/content/582a7643731bd.jpg">新生儿脐带护理</p>
<p>新生儿脐带的护理非常重要，如果不小心感染的话，宝宝会有感染破伤风、败血症等疾病。要隔绝病原体对宝宝的侵染，必须做好新生儿脐部的护理。</p>
<p>一般来说，脐带是会自己脱落的，但是期间存在一个时间的周期。在这期间，脐带就需要妈妈们小心护理，如能做到以下三个原则，就可以轻松照顾宝宝了。</p>
<p>1、保持脐带的干燥。在宝宝脐带自然脱落前应保持干燥，如果在洗澡时不慎将脐带根部弄湿，妈妈最好先以干净的小棉棒将该部位擦拭干净，再执行脐带护理。</p>
<p>2、避免摩擦脐带部位。宝宝在穿戴纸尿布的时候，一定要小心，不要让尿裤的腰际处在在脐带根部位置。否则，当宝宝活动时纸尿布极易摩擦到脐带根部，导致该部位破皮发红，甚至出血。</p>
<p>3、勿随意涂抹护肤霜。宝宝的脐带部位非常脆弱，绝对不能用面霜、乳液及油类涂抹脐带根部，以免脐带不易干燥甚至导致感染。</p>
<p>新生儿脐带出血怎么办</p>
<p>脐带在刚脱落时，肚脐的根部可能会出现渗血水的情况，这属于正常情况，家长不用太紧张。该伤口会像一般的伤口那样慢慢愈合。但如果宝宝经常大声哭闹，就有可能引发少量出血的情况，这就需要注意了。一般来说，宝宝肚脐出血需要注意4个问题：</p>
<p>1、脐带脱落的护理</p>
<p>一般地，宝宝出生后约7-10天，脐带就会干燥脱落。一般不会超过14天大部分的脐带都会很干脆地脱落下来，肚脐也会很快就干了。如果宝宝肚脐出血了，家长可以用75%的乙醇轻轻擦去血迹，再用消毒纱布包扎宝宝的脐部即可。一般几天后就可愈合，宝宝年幼，没有必要使用止血药，但应注意肚脐干燥。</p>
<p>2、保持肚脐的干净</p>
<p>肚脐的分泌物多，家长最好定时给宝宝清理肚脐。每次给宝宝洗澡后，要记得擦干肚脐部位的水分，可以用棉棒沾95%的酒精在肚脐处擦拭，但是不要随意把爽身粉撒在肚脐的周围。给宝宝换尿布时，也要注意检查脐部是否干燥，如果脐部是潮湿的，可重复上面的动作，以95%的酒精再次擦拭。干燥后的肚脐就容易脱落了。</p>
<p>3、脐息肉芽谨防细菌感染</p>
<p>宝宝在剪短脐带后，肚脐位置可能会出现脐息肉芽，一般来说，肉芽也会随脐带一样慢慢干化并脱落的。如果超过半个月，脐息肉芽还没有消退，妈妈可以找医生用化学物质破坏肉芽的生长，待几天后，脐息肉芽就会慢慢干化并掉落。 ...</p>
<p>新生儿脐带什么时候脱落</p>
<p>正常情况下，新生儿脐带会在2周内脱落，但是这也只是一个参考值。有快有慢的，有些宝宝7天左右就脱落了，但是有些宝宝快1个月了才脱落。</p>
<p>如果宝宝的脐带超过2周一直未脱落，家长们也不必紧张，只要每天坚持用酒精给宝宝消毒，看脐带局部干燥没有渗出就行。脐带干化后会自然脱落的。</p>
<p>新生儿脐带不脱落怎么办</p>
<p>宝宝出生后，医生会给宝宝的脐带扎结，几个小时后脐带残端会变成棕白色，以后逐渐干枯，变细，变成黑色，最终脱落。</p>
<p>正常情况下大部分新生儿脐带脱落为2周左右，假如2周后宝宝的脐带仍未脱落，家长们一定要仔细观察脐带的情况，是否存在感染、发炎的迹象，例如没有红肿或化脓；如果没有大量液体从宝宝的脐窝中渗出，爸妈就不用太担心，坚持每天用酒精给宝宝肚脐位置消毒即可。如果有出现异常情况，建议带宝宝到医院就诊。</p>
<p>如何保持新生儿脐带干爽</p>
<p>保持宝宝肚脐的干爽很重要，因为被扎结的脐带是一种坏死的组织，如果潮湿的话很容易感染上细菌的。该部位如果干化得快脐带也比较容易脱落，宝宝就不容易受到病菌的感染。因此保持肚脐干爽很有必要。</p>
<p>在照顾宝宝的过程中，如果脐带不慎被水或尿液浸湿，妈妈们应该马上用干棉球或干净柔软的纱布擦干，然后用酒精棉签消毒。</p>
<p>同时需要注意的是，在脐带脱落之前，不能让宝宝泡在浴盆里洗澡。保持宝宝肚脐处的干燥，也可以用纱布包围肚脐，保护宝宝肚脐位置的干爽。</p>
