<h1>
    菊花菜的功效与作用
</h1>
<p>
    <strong>【导读】</strong>蔬菜中的白菜，菜心，苋菜，空心菜等等日常中能常吃到。但是菊花菜就比较少见了。今天就来说说菊花菜吧，因为它富含钾元素，是人体内不可缺少的常量元素，正常成年人体内约含钾150克，以维持神经，肌肉的正常功能。人体一旦缺钾，正常的运动就会受到影响。 菊花菜的功效与作用
</p>
<p>
    1、保护眼睛。摄入菊花菜中的胡萝卜素，犹如添加了天然的眼药水，帮助保持眼角膜的润滑及透明度，保护眼睛晶体的纤维部分，促进眼睛的健康。还可以预防白内障。
</p>
<p>
    2、预防癞皮病。又叫糙皮病，是以皮肤、胃肠道、神经系统症状为主要表现。一般有：对称性皮炎，有色素沉着；口、舌炎症；腹泻；精神痴呆等。这与尼克酸的摄入、吸收减少及代谢障碍有关的疾病。
</p>
<p>
    而菊花菜中含有尼克酸。它参与身体内氧化还原过程，促进消化功能，维持皮肤和神经的健康，可以很好的预防癞皮病。
</p>
<p>
    3、消食开胃，通便利肺。菊花菜中含有特殊香味的挥发油，有助于宽中理气，消食开胃，增加食欲，并且其所含粗纤维有助肠道蠕动，促进排便，达到通腑利肠的目的。
</p>
<p>
    4、清血养心，润肺化痰。菊花菜内含丰富的维生素、胡萝卜素及多种氨基酸，性味甘平，可以养心安神，润肺补肝，稳定情绪，防止记忆力减退；此外，菊花菜气味清香，可以消痰开郁，避秽化浊。
</p>
<p>
    5、利小便，降血压。菊花菜中含有多种氨基酸、脂肪、蛋白质及较高量的钠、钾等矿物盐，能调节体内水液代谢，通利小便，消除水肿。在摄入高钠而导致高血压时，钾具有降血压作用。
</p>
<p>
    菊花菜的做法
</p>
<p>
    菊花菜鸡蛋汤
</p>
<p>
    材料：菊花菜、平菇、鸡蛋、姜、油盐、鸡精。
</p>
<p>
    1、菊花菜摘洗干净，浸泡十分钟。切生姜片。
</p>
<p>
    2、洗净平菇，把里面的水挤干。鸡蛋磕入碗中，搅拌均匀。
</p>
<p>
    3、锅里加一小盆水烧煮，等水开后，放油和生姜片，然后倒入平菇先煮一下。
</p>
<p>
    4、再放入洗净的菊花菜一起煮。
</p>
<p>
    5、把鸡蛋慢慢倒入锅中，成蛋花样。
</p>
<p>
    6、这时加盐和鸡精。搅拌一下，就可以出锅了。
</p>
<p>
    这道菜汤水无论从颜色还有口感来说都很不错。嫩滑可口，营养丰富。
</p>
<p>
    首先，菊花菜具有特殊的浓郁菊花芳香味，风味独特，稍甜，凉爽清口，含有铁、钙、磷、镁等人体必需的多种微量元素。
</p>
<p>
    其次，鸡蛋能健脑益智。鸡蛋黄中的卵磷脂、甘油三酯、胆固醇和卵黄素,对神经系统和身体发育有很大的作用。还可以保护肝脏。鸡蛋中的蛋白质对肝脏组织损伤有修复作用。蛋黄中的卵磷脂可促进肝细胞的再生,还可提高人体血浆蛋白量,增强肌体的代谢功能和免疫功能。
</p>
<p>
    最后，平菇含有的多种维生素及矿物质可以改善人体新陈代谢、增强体质、调节植物神经功能等作用，故可作为体弱病人的营养品。
</p>
<p>
    菊花菜的营养价值
</p>
<p>
    菊花菜的可食用部分达97%。每100g中所含能量85kg、水分93.1g、蛋白质1.8g，脂肪0.3g，膳食纤维2.2g，碳水化合物1. 7g，胡萝卜素1100μg、核黄素0.08mg、尼克酸0.4mg;维生素C 10mg、维生素E1.23mg;钾244mg,钠17.8mg,钙113mg,镁21 mg、铁1.7mg,锰0.09mg、锌0.26mg,铜0.07mg,磷35mg、硒6.09μg。
</p>
<p>
    菊花菜的茎和叶可以同食，有蒿之清气、菊之甘香，鲜香嫩脆的赞誉，一般营养成分无所不备，尤其胡萝卜素含量超过一般蔬菜，为黄瓜、茄子含量的1.5～30倍。
</p>
<p>
    菊花菜里含有多种氨基酸，所以菊花菜具有润肺补肝，稳定情绪，防止记忆力减退等作用，而且菊花菜里还含有粗纤维有助于肠道蠕动，能促进我们排便，从而可以达到通便利肠的目的。
</p>
<p>
    菊花菜里含有丰富的维生素，胡萝卜素等，气味芬芳，可以消痰止咳哦。菊花菜里含有蛋白质及较高的钠、钾等矿物元素，能够调节体内的水液代谢，消除水肿，菊花菜还含有一种挥发性的精油，以及胆碱等物质，具有降血压、补脑的作用。
</p>
<p>
    菊花菜的根、茎、叶、花都可作药材使用，有清血、养心、降压、润肺、清痰的功效。菊花菜具特殊香味，幼苗或嫩茎叶供生炒、凉拌、做汤来食用。
</p>
<p>
    菊花菜的适宜人群
</p>
<p>
    1、头痛、目赤。菊花菜茎叶性苦、辛、凉，有清热解毒，针对上火引起的头痛非常有效。
</p>
<p>
    2、便秘者。菊花菜里含有大量的膳食纤维。膳食纤维是人的健康饮食不可缺少的，它在保持消化系统健康上扮演着重要的角色，摄取足够的纤维也可以预防心血管疾病、癌症、糖尿病以及其它疾病。还可以清洁消化壁和增强消化功能，保护脆弱的消化道和预防结肠癌。膳食纤维对促进良好的消化和排泄固体废物有着举足轻重的作用。
</p>
<p>
    3、高血压。菊花菜里含有蛋白质及较高的钾等矿物元素，能够调节体内的水液代谢，消除水肿，菊花菜还含有一种挥发性的精油，以及胆碱等物质，具有降血压、补脑的作用。
</p>
<p>
    4、爱美人士。菊花菜含有很高的胡萝卜素。大量的β-胡萝卜素，摄入人体消化器官后，可以转化成维生素A，它可以维持眼睛和皮肤的健康，防止皮肤干燥、粗糙，改善夜盲症状况，有助于身体免受自由基的伤害。
</p>
<p>
    菊花菜孕妇能吃吗
</p>
<p>
    菊花菜有丰富的膳食纤维，膳食纤维对促进良好的消化和排泄固体废物有着非常得要的作用。特别是针对孕期的便秘。
</p>
<p>
    孕期为什么容易便秘呢？分析一下原因：
</p>
<p>
    1、缺乏运动。孕妇由于平时活动较少，肠道蠕动减慢，使食物残渣在肠道中滞留时间过长，水分被吸收，导致便秘。
</p>
<p>
    2、排便动力不足随着胎儿的日益成熟，子宫不断增大压迫肠道、月扇肌，腹肌运动受到限制，排便动力缺乏。
</p>
<p>
    3、不良的饮食习惯为了增加营养，孕妇会补充大量的肉类、蛋类、奶类食品，而忽视了蔬菜、水果、粗粮和高纤维食物的摄取量，导致饮食中缺乏B族维生素、钾或铁。有些孕妇喜欢吃辛辣食物，也易导致便秘。
</p>
<p>
    4、激素分泌增加怀孕期间由于黄体素的分泌增加，使得消化道的肌肉松弛，肠道蠕动减缓及大肠对水分的吸收增加，致使粪便较硬，而导致排便不畅。
</p>
<p>
    所以针对孕妇便秘的原因，孕妇可以吃菊花菜，一次不要食用过量。因为菊花菜性凉。孕妇应当尽可能保证蛋白质的量，适当补充肉类、奶类、蛋类、豆类、蔬菜、水果等等食物的均衡摄入。要合理饮食才能保证妈妈和宝宝的健康！
</p>
