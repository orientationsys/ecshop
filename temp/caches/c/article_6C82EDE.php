                   <div class="stk-post stk-layout_nocol stk-theme_1445"><h1>小孩睡觉打鼾</h1>
<p><b>【导读】</b>神啊！家里一大一小两个呼噜王啊？做妈的要跳起来了吧？从医学上讲，睡觉打呼噜或打鼾又叫阻塞性睡眠呼吸暂停综合症，是睡眠过程中发生的一种呼吸障碍。小家伙睡觉打鼾多是因为鼻咽部腺样体及扁桃体肥大或者有颅面结构的发育畸形。这样会使睡眠质量下降，影响身体的发育，特别是在智力方面，影响更大。所以要是你家小孩睡觉打鼾就得及早治疗，同时注意饮食平衡，保持作息时间有规律性呀！ <img src="../../../storage/images/content/582a7ae594ec9.jpg">小孩睡觉打鼾是怎么回事</p>
<p>对于小孩子睡觉打鼾的问题可大可小，但事出有因，所以肯定也是有什么我们所没有及时发现的原因导致小孩睡觉打鼾的。父母在发现小孩睡觉打鼾时千万不可大意，要注意是什么原因导致的，因为有些病症会引起小孩在睡觉时打鼾，甚至有可能引起睡眠呼吸障碍症。</p>
<p>打鼾也叫呼噜，可分为良性打鼾和恶性打鼾两种，良性打鼾通常是因为身体劳累，呼噜声均匀，一般不需要治疗，休息好后就会自愈。而恶性打鼾则是呼噜声断断续续不均匀，呼吸暂停，通常是入睡时呼吸受阻造成的。</p>
<p>造成小孩打鼾的原因有多种，例如遗传，如果说爸爸妈妈的颌骨异常、呼吸道狭窄的话，通常儿女也会有相同的症状，所以在睡眠的过程中呼吸会受到阻碍而导致打鼾。通常小孩睡觉打鼾的最主要原因是小孩呼吸道周边的一些腺体发生病变引起的，例如扁桃体或腺样体在发炎时会出现肿大现象，而孩子在入睡后肌肉放松，肿大的腺体就会暂时阻塞气道，通气受阻使得睡眠时不能经鼻呼吸而出现张口呼吸，结果舌根后坠，随呼吸发出鼾声。</p>
<p>如何预防小孩睡觉打鼾</p>
<p>父母在发现孩子睡觉有打鼾现象时要及早的治疗，日常的饮食均衡要注意，还要保持有规律的作息时间。若是孩子睡觉打鼾并且呼吸不畅时，父母就需要仔细观察孩子的情况，必要时到医院做个检查。通常都是腺体的病变引起的，所以可以通过手术来进行治疗。如扁桃体的摘除，腺体的刮除，上下颌的整形矫正，还有鼻腔的手术等，都是治疗儿童呼吸障碍的有效措施。</p>
<p>在日常生活中，妈妈们也可以注意一下几点：</p>
<p>1、在孩子的饮食上要注意均衡膳食，10个月的宝宝要及时添加辅食，增加食物的多样性，合理喂养。</p>
<p>2、增强小孩的体质也可以减少呼吸道感染的几率。很多妈妈怕宝宝晒到太阳，都会选择在傍晚太阳下山的时候才带宝宝出去散步，其实宝宝多晒晒阳光对宝宝反而更好，而且早上的空气也更新鲜；别总是抱着宝宝，让宝宝多做做爬行游戏，也可以让宝宝身体更结实起来。</p>
<p>3、要注意宝宝的鼻子，是否里面有异物堵塞鼻孔，及时清理鼻涕等分泌物，保持鼻子的通畅。</p>
<p>4、更换宝宝的睡姿，若宝宝睡觉打鼾时可以也宝宝换个睡姿或把头部适当的垫高。</p>
<p>宝宝的打鼾症状较严重时要及时到医院就诊，若是因为腺体发炎的话可以选择手术治疗。要注意孩子的营养问题和身体素质，也要防止营养过剩而导致的肥胖和作息时间的规律，减少夜间的激烈活动。 ...</p>
<p>小孩睡觉打鼾的注意事项</p>
<p>孩子不同于大人，各个身体器官都不成熟，还处于生长发育的阶段，所以孩子在睡觉时打鼾也需要家长朋友多多注意，不要将孩子的一些症状误认为是小问题而不加以重视。</p>
<p>很多家长在发现孩子晚上睡觉不安稳、多用口呼吸、白天嗜睡等问题归咎为孩子年龄小、身体虚弱所致，所以往往会选择给孩子补充营养。实际上，孩子晚上睡觉多动不安稳、张口呼吸、白天嗜睡等情况不是身体虚，而是腺样体肥大堵塞鼻孔，从而引起小孩晚上睡觉时呼吸的不顺畅而张开呼吸，也会引发孩子心绪烦躁、易发怒，有时还会出现攻击倾向的行为。</p>
<p>一般我们在感冒时会鼻塞，睡觉时张口呼吸，所以孩子经常张口呼吸时家长会以为是孩子鼻塞，但孩子没有感冒又怎么鼻塞呢？专家指出，鼻部的症状也是腺样体肥大的临床表现的局部症状之一，不过多数家长却常以为孩子是感冒或是鼻炎等。实际上腺样体肥大通常伴有鼻炎、鼻塞及流鼻涕等症状，说话时带有鼻塞性鼻音，睡觉时也会发出鼾声。</p>
<p>对于小孩来说，家长都是希望小孩可以少动针动刀，所以在治疗腺样体肥大时都是希望可以通过药物治疗来达到治疗效果，但事实上药物治疗对腺样体肥大症状没有多大的缓解作用，而且儿童在用药上也需要谨慎。所以在治疗腺样体肥大时还的推荐选择手术治疗，更快的解决病情。 ...</p>
<p>小孩睡觉打鼾的危害</p>
<p>小孩睡觉打鼾时，大人一般不会去过多在意，认为孩子是睡得香才会打鼾的，但其实小孩睡觉打鼾并不是都是好的症状，小孩睡觉打鼾也是有危害的。</p>
<p>小孩打鼾多是因为鼻咽部腺样体及扁桃体肥大或者有颅面结构的发育畸形而引起的，病变的腺样体或扁桃体容易使小孩入睡后发生呼吸困难或暂停，从而出现缺氧，影响小孩的正常发育和学习，严重的打鼾还可以引起小儿痴呆。所以如果孩子有出现睡觉打鼾，并且上课时注意力不集中、嗜睡、记忆力下降、学习成绩差等现象，要及时到医院检查鼻咽及扁桃体。如果确认问题所在时，就要及时进行相应的治疗。</p>
<p>小孩睡觉打鼾不仅导致睡眠质量的下降，还会影响身体的发育，特别是在智力方面，影响更大。睡眠质量的好与坏对处于生长阶段的小孩来讲是很重要的，孩子的生长激素大部分是在深入睡眠时大脑释放出来的，以促进孩子各个系统的生长发育，而睡觉打鼾引起的睡眠呼吸障碍对小孩的睡眠情况危害很大，会造成睡眠时间的改变，使睡眠的连续性中断，睡眠质量下降，睡眠质量一旦下降，大脑分泌的生长激素也会相对于的减少，从而影响到孩子的发育。</p>
<p>打鼾而引起的呼吸暂停会使大脑处于缺氧的状态，对孩子的神经系统的发育影响重大，有可能导致孩子智力发育不好，也有可能影响到大脑的替他方面甚至包括心脏。 ...</p>
<p>小孩睡觉的声音预示健康问题</p>
<p>宝宝在睡眠中除了会发出打鼾声之外，还会经常发出其他的各种各样的响声。在大人的认知中睡梦中出声是司空见惯的事情，所以会忽略掉小孩睡觉时发出的声响。其实宝宝在睡觉时发出的声响也预示着宝宝的健康问题，以下为大家列举几项，希望家长朋友们可以多多注意自家宝宝是否有相似的情况。</p>
<p>1、有些宝宝在睡觉时会发出类似小鸡的叫声，医学上称之为先天性喉鸣。先天性喉鸣产生的原因是宝宝的喉软骨发育不好，喉部组织软弱松弛、吸气时候组织塌陷、喉腔变小所引起的。症状不大的话一般在2到3岁时就能自愈，若是严重的话就需要进行气管切开手术。所以平时要注意宝宝受凉及受惊的情况，以免宝宝发生呼吸道感染和喉痉挛，加剧喉阻塞，同时调整宝宝睡眠时的体位，取侧卧位也可减轻症状。</p>
<p>2、在婴儿照看时最艰难的阶段就是半夜，很多宝宝在半夜都会惊哭，困倦的家长只能起来照看孩子。一般情况下，宝宝在缺钙时会比较容易哭闹，所以家长要为宝宝补充足够的维生素D和钙剂，让宝宝多接触到阳光。也有些宝宝也哭是因为疾病的原因，宝宝感冒时或是其他疾病的患病期间宝宝也会比平常容易哭闹，所以这个时候要尽快治好宝宝的原发病，宝宝和家长们也能安然入睡。</p>
<p>3、宝宝发育到一定阶段时候就会开始张牙齿，这个时候宝宝也会有一些磨牙的习惯。宝宝磨牙可以归结为几点：牙齿发育不良、消化功能紊乱、营养不均衡、肠道有寄生虫等。只要针对宝宝的病症去对症下药就能解决宝宝晚上睡觉磨牙的习惯。 ...</p>
