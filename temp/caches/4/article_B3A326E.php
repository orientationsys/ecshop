<p>
    宝宝流鼻涕
</p>
<p>
    宝宝流鼻涕的时候很容易会导致呼吸困难，这时候是很危险的。要想好好地处理宝宝流鼻涕的症状，那么妈妈们要先了解宝宝流鼻涕的原因。
</p>
<p>
    1、感冒引起流鼻涕。感冒引起的鼻炎被称为急性鼻炎，此时鼻腔粘膜充血肿胀，腺体分泌增多即形成鼻涕，开始为清水样的，3-5天后渐为脓涕，以后逐渐痊愈。
</p>
<p>
    2、过敏性鼻炎。常流清涕并伴有鼻塞、鼻痒、打喷嚏等症状，尤其清晨起床后明显，从温暖的被窝中出来，立即连连打喷嚏，接着清水鼻涕流个不停，需警惕宝宝是否患有过敏性鼻炎。支气管哮喘同时伴有流鼻涕，妈妈更是要注意宝宝是否患有过敏性鼻炎了。
</p>
<p>
    3、鼻腔内有异物。如果宝宝的一侧鼻腔有臭味，流脓涕，有时涕中带血丝，需考虑鼻腔内是否有异物。这种情况多发生于3岁左右的孩子，玩耍时因好奇，常常自己把纸张、豆类、等异物放入鼻腔内，塞入后取不出而产生臭味，但金属类小零件、小纽扣塞入鼻腔后不一定引起臭味。
</p>
<p>
    4、急慢性鼻炎。宝宝因急慢性鼻炎继发鼻窦炎时，常常鼻涕会很多，似乎“流不完”，有时还伴有头痛。
</p>
<p>
    5、鼻息肉。个别宝宝仅单侧有鼻涕，但擤也擤不出来，鼻孔不通气，睡觉打呼噜，那就要警惕有鼻息肉的存在了。
</p>
<p>
    宝宝流鼻涕怎么缓解
</p>
<p>
    碰到流鼻涕的时候，妈妈们都会自然地拿起纸巾或手帕来帮宝宝擦掉。可是擦得次数多了会发现宝宝的皮肤也磨损了。可是看得宝宝难受的样子妈妈们真的有点不知所措。宝宝流鼻涕的时候，妈妈们可以尝试用以下方法缓解。
</p>
<p>
    1、热敷：用湿热的毛巾，在宝宝的鼻子上进行热敷。由于鼻黏膜遇热收缩后，鼻腔会比较通畅，黏稠的鼻涕也较容易水化而流出来。在热敷时要保持动作轻柔，如果发现宝宝的鼻孔里有鼻屎，可以先用棉花棒蘸水清洁;也可以慢慢地按摩宝宝的鼻子或鼻翼两边。
</p>
<p>
    2、蒸脸器：蒸脸器对着宝宝的脸，让他接触湿气和热气。蒸汽可湿润宝宝的鼻腔，将大量的鼻涕快速、自然地排出。使用时蒸脸器不要太靠近宝宝，以免伤害到他娇嫩的皮肤;而且一次使用的时间不宜太长，约3分钟即可。
</p>
<p>
    3、吸鼻器：当宝宝鼻子的分泌物很多、鼻音重时，妈妈可以使用吸鼻器，使用时要注意，吸一边鼻孔时，要同时按压住另一个鼻孔，效果会比较好。另外吸鼻器一次可以吸取大量的鼻涕和分泌物。使用前，建议家长先检查宝宝的鼻腔内是否有鼻屎，动作保持轻柔，避免太深入宝宝的鼻腔，造成疼痛或受伤，使用完要清洗干净。
</p>
<p>
    4、精油热敷：取精油滴在热毛巾或小手帕上，敷在宝宝的鼻子上；或用精油喷雾，喷在空气当中。
</p>
<p>
    5、滴母乳：妈妈将挤出的母乳，滴进宝宝的鼻孔里。 ...
</p>
<p>
    宝宝流鼻涕怎么治疗
</p>
<p>
    流鼻涕多伴有鼻塞症状，有的还表现有头昏、头痛、嗅觉减退、说话带有鼻音等，如果不积极治疗，还可引起中耳炎、气管炎、肺炎、心肌炎、肾炎等并发症。因此，孩子常流鼻涕一定要重视。不同的鼻涕，又有不同的治疗方法。
</p>
<p>
    一、急性鼻炎(即感冒)
</p>
<p>
    1、孩子患感冒时，饮食宜清淡，易消化，要多让孩子喝水。
</p>
<p>
    2、注意室内通风。
</p>
<p>
    3、发热时要适当休息，如果病情较重，最好卧床休息。
</p>
<p>
    4、在冬春感冒高发季节，可给孩子服用姜糖大枣汤〔生姜10克、大枣10克、红糖70克水煎服〕，可以预防感冒。
</p>
<p>
    二、急、慢性鼻窦炎
</p>
<p>
    治疗急性鼻窦炎，要用足量的抗生素来控制感染，防止转为慢性。鼻腔局部点药可使用盐酸羟甲唑琳喷鼻剂、3%~3.5%的高渗盐水等。
</p>
<p>
    而慢性鼻窦炎以局部治疗为主，可选用盐酸羟甲唑啉喷鼻剂、3%~3.5%的高渗盐水、辅舒良喷鼻剂、雷诺考特喷鼻剂、伯克纳喷鼻剂等。但血管收缩剂滴鼻不宜长期使用，以免发生药物性鼻炎。另外可选用鼻渊丸、鼻渊舒口服液、鼻炎丸等口服中成药，疗程一定要足够。
</p>
